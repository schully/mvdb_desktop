    private void saveButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveButtonMousePressed
        try {                                        
            // TODO add your handling code here:
            
            MovieData formData = new MovieData();
            
            formData.setId(MovieId);
            formData.setAgeRated(updateAgeRated.getText());
            
            formData.setImdbScore(Double.parseDouble(updateImdbScore.getText()));
            formData.setPlot(updatePlot.getText());
            formData.setRuntime(Integer.parseInt(updateRuntime.getText()));
            formData.setTitle(updateTitle.getText());
            formData.setPossessor(updatePossessor.getText());
           
            try {
                MovieAddService.getMasIn().submit(formData, true);
            } catch (Exception ex) {
                Logger.getLogger(AddFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (Exception ex) {
            Logger.getLogger(EditFilm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonMousePressed

